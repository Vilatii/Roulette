	int rate; //ставка
	int number; //число
	int res; //запись результата
public:
	Roulette() {
	}; //конструктор
	Roulette(int Rate, int Number) { // конструктор  с параметрами
		setRate(Rate); // устанавливаем ставку
		setNumber(Number); //устанавливаем число
	}
